    public void updateTable(List<Producto> lista){
        DefaultTableModel model = (DefaultTableModel) tblProducto.getModel();
        model.setNumRows(0);
        for(Producto p : lista){
            model.addRow(new Object[]{
                p.getNombre(), p.getPrecioUnitario(), p.getStock()
            });
        }
    }  

