    private void btnCarritoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCarritoMouseClicked

        if(fila!=-1){
                String nombre = String.valueOf(tblProducto.getValueAt(fila, 0));
                int stock = Integer.parseInt(String.valueOf(tblProducto.getValueAt(fila, 2)));
                int cantidad = (int) jspCantidad.getValue();
                if(stock<cantidad){
                        JOptionPane.showMessageDialog(this, "Solo nos quedan "+stock+" "+nombre+" lo sentimos");
                }else{
                    for(Producto p: productos){
                        if(p.getNombre().equalsIgnoreCase(nombre) && p.getStock()>=cantidad && cantidad!=0){
                        carrito.add(new DetalleVenta(p, cantidad));
                        p.setStock(p.getStock()-cantidad);
                            updateTable(productos);
                        }else if(cantidad==0){
                            JOptionPane.showMessageDialog(this, "Selecciona la cantidad a comprar");
                        }

                    }
                }
        }else{
            JOptionPane.showMessageDialog(this, "Selecciona un producto");
        }
        
    }//GEN-LAST:event_btnCarritoMouseClicked

